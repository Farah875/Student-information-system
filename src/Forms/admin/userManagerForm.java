    public void refreshTable(int type)
    {
         DefaultTableModel modelPolygon = new DefaultTableModel(new Object[]
        {"id","First name","Last Name","Username", "Phone", "Email","password"},0);
        for ( User user : repo.getAllUsers(type))
        {
            modelPolygon.addRow(new Object[] {user.getID(),user.getFirstName(),user.getLastName(),
            user.getUsername(), user.getPhone(),user.getEmail(),user.getPassword() });
        }
       

             this.usersTable.setModel(modelPolygon);
             this.usersTable.getColumnModel().getColumn(0).setWidth(0);
             this.usersTable.getColumnModel().getColumn(0).setMinWidth(0);
             this.usersTable.getColumnModel().getColumn(0).setMaxWidth(0);
             
        //highlighting the selected type of users 
        switch (type)
        {
            case 0: adminLabel.setBackground( new Color(249,226,132));
            break;
            
            case 1: teacherLabel.setBackground( new Color(249,226,132));
            break;
            
            case 2: studentLabel.setBackground( new Color(249,226,132));
            break;
            default: break;
        }
           
    }

