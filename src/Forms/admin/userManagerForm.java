    private void removeBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeBtnMouseClicked
        
        int column = 3;
        int row = this.usersTable.getSelectedRow();
        if(row >= 0)
        {
            String username = this.usersTable.getValueAt(row, column).toString();
            int result = repo.deleteUser(logged, username );
            switch (result)
            {
                case 0:  JOptionPane.showMessageDialog(null,"User deleted successfuly");
                    return;
                case 1:  JOptionPane.showMessageDialog(null,"Error deleting please try again later");
                    return;
                case 3: JOptionPane.showMessageDialog(null,"the username you entered is not available in the database");
                    return;
                default: return;
            
            }  
        }
        
        
        refreshTable(type);
    }//GEN-LAST:event_removeBtnMouseClicked

    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        
        User userAdd = new User();
        userAdd.setFirstName(this.firstName.getText());
        userAdd.setLastName(this.lastName.getText());
        userAdd.setEmail(this.email.getText());
        userAdd.setPhone(this.phone.getText());
        userAdd.setPassword(this.password.getText());
        userAdd.setType(type);
        
        int result = repo.addUser(logged, userAdd);
        
        switch (result)
            {
                case 0:  JOptionPane.showMessageDialog(null,"User added successfuly");
                    refreshTable(type);
                    break;     
                case 1:  JOptionPane.showMessageDialog(null,"Error creating user please try again later");
                    break;
                case 2: JOptionPane.showMessageDialog(null,"the user type you entered is not valid");
                    break;
                case 3: JOptionPane.showMessageDialog(null,"you don't have enough privilages");
                   break;
                default: break;
            
            }
          refreshTable(type);
    }//GEN-LAST:event_addBtnMouseClicked

